print("Programa que identifica el tipo de dato de un valor ingresado por el ususario,\
 se realizarán cinco interacciones")
n=1
while n<=5:
    if n==1:
        it1=input("\nPrimera interacción, ingrese un valor cualquiera: ")
        print("\nEste tipo de dato en Python es:")
        print(type(it1))
    elif n==2:
        it1=input("\nSegunda interacción, ingrese un valor cualquiera: ")
        print("\nEste tipo de dato en Python es:")
        print(type(it1))
    elif n==3:
        it1=input("\nTercera interacción, ingrese un valor cualquiera: ")
        print("\nEste tipo de dato en Python es:")
        print(type(it1))
    elif n==4:
        it1=input("\nCuarta interacción, ingrese un valor cualquiera: ")
        print("\nEste tipo de dato en Python es:")
        print(type(it1))
    elif n==5:
        it1=input("\nQuinta interacción, ingrese un valor cualquiera: ")
        print("\nEste tipo de dato en Python es:")
        print(type(it1))
    n=n+1
